              <el-input v-model="ruleForm.wenzhangbianhao" placeholder="文章编号" readonly></el-input>
          </el-form-item>
          <el-form-item :style='{"padding":"10px","margin":"0 0 10px 0","alignItems":"center","background":"linear-gradient(90deg, rgba(238,177,238,1) 0%, rgba(165,228,222,1) 30%, rgba(165,228,222,1) 100%)","display":"flex"}' label="文章标题" prop="wenzhangbiaoti">
            <el-input v-model="ruleForm.wenzhangbiaoti" 
                placeholder="文章标题" clearable ></el-input>
          </el-form-item>
          <el-form-item :style='{"padding":"10px","margin":"0 0 10px 0","alignItems":"center","background":"linear-gradient(90deg, rgba(238,177,238,1) 0%, rgba(165,228,222,1) 30%, rgba(165,228,222,1) 100%)","display":"flex"}'  label="文章分类" prop="wenzhangfenlei">
            <el-select v-model="ruleForm.wenzhangfenlei" placeholder="请选择文章分类"  >
              <el-option
                  v-for="(item,index) in wenzhangfenleiOptions"
                  :key="index"
                  :label="item"
                  :value="item">
              </el-option>
            </el-select>
          </el-form-item>
          <el-form-item :style='{"padding":"10px","margin":"0 0 10px 0","alignItems":"center","background":"linear-gradient(90deg, rgba(238,177,238,1) 0%, rgba(165,228,222,1) 30%, rgba(165,228,222,1) 100%)","display":"flex"}' label="文章封面" v-if="type!='cross' || (type=='cross' && !ro.wenzhangfengmian)" prop="wenzhangfengmian">
            <file-upload
            tip="点击上传文章封面"
            action="file/upload"
            :limit="3"
            :multiple="true"
